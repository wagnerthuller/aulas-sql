
USE BD_INDEX
GO

CREATE TABLE TB_INDEX
(
ID TINYINT,
NOME VARCHAR(20)
)

-- CRIANDO INDEX CLUSTERIZADO

CREATE CLUSTERED INDEX CIX_ID
ON DBO.TB_INDEX
(ID ASC)

-- INDEX N O CLUSTERIZADO

CREATE NONCLUSTERED INDEX NCIX_NOME
ON DBO.TB_INDEX
(NOME)

-- DESABILITANDO UM IDEX
ALTER INDEX CIX_ID
ON DBO.TB_INDEX DISABLE;

-- DESABILITAR UM INDEX
ALTER INDEX NCIX_NOME
ON DBO.TB_INDEX DISABLE;


-- DELETANDO UM INDEX 
DROP INDEX CIX_ID
ON DBO.TB_INDEX;


-- DELETANDO UM INDEX 
DROP INDEX NCIX_NOME
ON DBO.TB_INDEX;